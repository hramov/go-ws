package utils

func Parser(x int) string {
	var a string
	switch x {
	case 1:
		a = "A"
		break
	case 2:
		a = "Б"
		break
	case 3:
		a = "В"
		break
	case 4:
		a = "Г"
		break
	case 5:
		a = "Д"
		break
	case 6:
		a = "Е"
		break
	case 7:
		a = "Ж"
		break
	case 8:
		a = "З"
		break
	case 9:
		a = "И"
		break
	case 10:
		a = "К"
		break
	}
	return a
}
